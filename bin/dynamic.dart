void main(List<String> args) {
  // Penulisan menggunakan dynamic
  dynamic variable = 10;
  print(variable);

  variable = true;
  print(variable);

  variable = "Berisi string";
  print(variable);

  // Penulisan deklarasi var
  var dynamiVar;

  dynamiVar = 10;
  print(dynamiVar);

  dynamiVar = "Berubah string";
  print(dynamiVar);

  dynamiVar = true;
  print(dynamiVar);
}
